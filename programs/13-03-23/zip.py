# zip() : it will combine two or more listes with combination of respect elements
# it will return zip object

# as a result element it will return the combine tuple for respective lists
# note :- the length of the zip will be the same as the length of least element the list

# Syntax : zip (l1,l2,l3,...,ln)

l1=[1,2,3]
l2=["one","two","three"]

print(list(zip(l1,l2)))